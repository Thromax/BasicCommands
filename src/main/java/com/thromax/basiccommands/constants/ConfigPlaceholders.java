package com.thromax.basiccommands.constants;

/**
 * BasicCommands configuration placeholders stored in constants
 */
public final class ConfigPlaceholders {

    /**
     * Placeholder used in config.yml for command sender's name
     */
    public static final String SENDER_NAME_PLACEHOLDER = "%sender%";
    /**
     * Placeholder used in config.yml for command's target.
     * This can be various things, from a player or worlds's name to a gamemode.
     */
    public static final String TARGET_PLACEHOLDER = "%target%";

}
